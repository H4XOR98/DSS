    // Button Alterar Categoria
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        if (index != -1){
            String conteudo = jList1.getSelectedValue().toString();
            String[] parts = conteudo.split(" | ", 2);
            String novaCategoria = jComboBox1.getSelectedItem().toString();
            this.facade.mudarCategoriaNova(Integer.parseInt(parts[0]), novaCategoria);
            List<String> conteudos = this.facade.listaConteudo();
            DefaultListModel<String> model = new DefaultListModel<>();
            jList1.setModel(model);
            for (String s : conteudos){
                model.addElement(s);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
