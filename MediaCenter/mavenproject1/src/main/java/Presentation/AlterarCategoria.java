    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        if (index != -1){
            String conteudo = jList1.getSelectedValue().toString();
            String[] parts = conteudo.split(" | ", 2);
            if (this.facade.tipoConteudo(Integer.parseInt(parts[0])) == 1){
                jComboBox1.removeAllItems();
                for (String s : this.musicas) jComboBox1.addItem(s);
            }
            else {
                jComboBox1.removeAllItems();
                for (String s : this.videos) jComboBox1.addItem(s);
            }
        }
    }//GEN-LAST:event_jList1MouseClicked

    
