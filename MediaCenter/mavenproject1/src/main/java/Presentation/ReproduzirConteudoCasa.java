    // Button Reproduzir
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int index; 
        index = jList1.getSelectedIndex();
        String opcoes [] = new String[2];
        if (index != -1){
            String conteudo = jList1.getSelectedValue().toString();
            String[] parts = conteudo.split(" | ", 2);
            opcoes[0] = "especifico";
            opcoes[1] = parts[0];
        }
        else{
            String tipo = jComboBox1.getSelectedItem().toString();
            opcoes[0] = tipo;
            if (!tipo.equals("aleatório")){
                String opcao = jComboBox2.getSelectedItem().toString();
                opcoes[1] = opcao;
            }
        }
        Playlist playlist;
        JFrame frame = new JFrame("Aviso");
        try{
            playlist = this.facade.criarPlaylist(opcoes);
            this.facade.reproduzir(playlist);
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(frame, e.getMessage());
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

    
