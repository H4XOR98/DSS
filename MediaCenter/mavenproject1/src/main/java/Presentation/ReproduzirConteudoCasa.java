    // ComboBox Tipo de Reprodução
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        List<String> listaArtistas = this.facade.listaAutores();
        List<String> listaCategorias = this.facade.listaCategorias();
        jList1.clearSelection();
        String tipo = jComboBox1.getSelectedItem().toString();
        if (tipo == "aleatório") jComboBox2.removeAllItems();
        if (tipo == "por autor"){
            jComboBox2.removeAllItems();
            for (String s : listaArtistas) jComboBox2.addItem(s);
        }
        if (tipo == "por categoria"){
            jComboBox2.removeAllItems();
            for (String s : listaCategorias) jComboBox2.addItem(s);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    
